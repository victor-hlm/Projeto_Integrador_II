    private void atualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizarActionPerformed

        if ( (livro.getText().equals("")) && (autor.getText().equals("")) && (titulo.getText().equals("")) && (isbn.getText().equals("")) && 
             (editora.getText().equals("")) && (volume.getText().equals("")) && (quantidade.getText().equals("")) && (status.getText().equals("")) ){

            JOptionPane.showMessageDialog(null, "Preencha novamente todos os campos!!");
        }
        else{

            try { // Tramento de erros.

                String L = livro.getText();
                String A = autor.getText();
                String T = titulo.getText();
                String I = isbn.getText();
                String E = editora.getText();
                String V = volume.getText();
                String Q = quantidade.getText();
                String S = status.getText();
                
                String url = "jdbc:mysql://localhost/db_livros";
                String sql = "UPDATE livro SET Autor = '"+A+"', Titulo = '"+T+"', ISBN = '"+I+"', Editora = '"+E+"', Volume = '"+V+"', Quantidade = '"+Q+"', Status = '"+S+"' WHERE Titulo = '"+L+"'";

                Connection conexao = DriverManager.getConnection(url, "root","");

                PreparedStatement editar = conexao.prepareStatement(sql);

                editar.executeUpdate();

                JOptionPane.showMessageDialog(null,"Atualizado com sucesso!");
            }

            catch(Exception erro) {

                JOptionPane.showMessageDialog(null,"Erro na ConexÃ£o com Banco de Dados: "+erro);
            }

        }
    }//GEN-LAST:event_atualizarActionPerformed

